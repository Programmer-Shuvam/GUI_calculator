B7=Button(w,text="7",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:display("7"))
B8=Button(w,text="8",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:display("8"))
B9=Button(w,text="9",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:display("9"))
