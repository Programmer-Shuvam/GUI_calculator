from tkinter import *
w=Tk()
w.title("Calculater")
v=StringVar()
def display(x):
    t=v.get()
    v.set(t+x)
def equal():
    t=v.get()
    e=eval(t)
    v.set(e)
def clear():
    v.set("")
def bs():
    t=v.get()
    v.set(t[0:-1])
def percent():
    t=v.get()
    p=int(t)
    v.set(p/100)
B1=Button(w,text="1",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:display("1"))
