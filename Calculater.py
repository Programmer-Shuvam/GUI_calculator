Bs=Button(w,text="-",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:display("-"))
Bm=Button(w,text="*",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:display("*"))
Bd=Button(w,text="/",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:display("/"))
Bl=Button(w,text="%",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:percent())
Be=Button(w,text="=",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:equal())
Bc=Button(w,text="C",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=150,bd=10,command=lambda:clear())
Bp=Button(w,text=".",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:display("."))
Bb=Button(w,text="<-",font=("Edwardian Script ITC Regular",45,"bold"),padx=5,pady=5,bd=10,command=lambda:bs())
E=Entry(w,bg='light green',font=("Edwardian Script ITC Regular",45,"bold"),textvariable=v,width=15,bd=10)
E.grid(row=1,columnspan=6)
